document.getElementById("imgTamarindChicken").addEventListener('mouseover', (event) => {
    document.getElementById('textTamarindChicken').style.textDecoration = 'underline'
});

document.getElementById("imgTamarindChicken").addEventListener('mouseout', (event) => {
    document.getElementById('textTamarindChicken').style.textDecoration = 'none'
});

document.getElementById("imgTamarindChicken").addEventListener('click', (event) => {
    window.location.href = "../Recipe_Tamarind_Chicken/recipe.html";
});

